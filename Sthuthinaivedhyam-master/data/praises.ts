import type { PraiseItem, PraiseSection } from '@/types/praise';

// Generate initial praise items
function generatePraiseItems(startId: number, endId: number): PraiseItem[] {
  const items = [];
  for (let id = startId; id <= endId; id++) {
    items.push({
      id,
      text: `Praise ${id}`,
      reference: `Reference ${id}`,
    });
  }
  return items;
}

